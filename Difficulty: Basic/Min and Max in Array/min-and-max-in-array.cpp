class Solution {
  public:
    pair<int, int> getMinMax(vector<int> arr) {

        priority_queue<int>max;
        priority_queue<int,vector<int>,greater<int>>min;
        for(int i = 0 ; i < arr.size() ; i++)
        {
            max.push(arr[i]);
            min.push(arr[i]);
        }
        return {min.top(),max.top()};
    }
};

